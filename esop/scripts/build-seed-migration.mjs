// Reads assets/data.json and emits a deterministic SQL migration that seeds
// events. Re-running produces byte-identical output, and the migration is
// idempotent via `on conflict do nothing`.
//
// Usage: node scripts/build-seed-migration.mjs
//
// Event id formula: 'ev_seed_' + sha256(seed_index || type || payload).slice(0,16)
// Hash chain: each row's hash is sha256(prev_hash || type || at || payload)
// where prev_hash comes from the row written just before it.

import { readFileSync, writeFileSync } from "node:fs";
import { createHash } from "node:crypto";

const data = JSON.parse(readFileSync("assets/data.json", "utf8"));
const at = `${data.meta?.as_of ?? new Date().toISOString().slice(0, 10)}T00:00:00Z`;

const events = [];

// 1. FY2025 draft grants (one event per holder with a non-zero draft qty).
for (const h of data.holders ?? []) {
  for (const g of h.grants ?? []) {
    if (g.status === "active") {
      events.push({
        type: "grant_approved",
        payload: {
          holder_id: String(h.id),
          fy: g.fy,
          grant_date: g.grant_date,
          letter_date: g.grant_date,
          qty: g.qty,
        },
      });
    }
  }
}

// 2. Valuation history.
for (const v of data.valuation_history ?? []) {
  events.push({ type: "valuation_added", payload: v });
  if (v.active) events.push({ type: "valuation_activated", payload: { fy: v.fy } });
}

// 3. Data-quality issues as informational events so they surface on admin page.
for (const issue of data.data_quality ?? []) {
  events.push({ type: "data_quality_flagged", payload: issue });
}

// 4. Special dividends (single event each).
for (const d of data.special_dividends ?? []) {
  events.push({ type: "dividend_declared", payload: d });
}

// Compute deterministic ids and inline prev_hash via subqueries.
const ids = events.map((e, i) => {
  const stable = `${i}|${e.type}|${JSON.stringify(e.payload)}`;
  return "ev_seed_" + createHash("sha256").update(stable).digest("hex").slice(0, 16);
});

const lines = [
  "-- Auto-generated by scripts/build-seed-migration.mjs",
  "-- Idempotent via `on conflict do nothing`. Re-running is safe.",
  `-- Generated ${events.length} seed events from assets/data.json.`,
  "begin;",
];

events.forEach((e, i) => {
  const id = ids[i];
  const payloadEsc = JSON.stringify(e.payload).replace(/'/g, "''");
  const prevSelect = i === 0 ? "''" : `coalesce((select hash from public.events where id = '${ids[i - 1]}'),'')`;
  const prevInsert = i === 0 ? "null" : `(select hash from public.events where id = '${ids[i - 1]}')`;
  lines.push(
    `insert into public.events(id, type, payload, at, actor_role, prev_hash, hash)
values('${id}', '${e.type}', '${payloadEsc}'::jsonb, '${at}', 'system',
  ${prevInsert},
  encode(digest(${prevSelect} || '${e.type}' || '${at}' || '${payloadEsc}', 'sha256'), 'hex'))
on conflict (id) do nothing;`,
  );
});

lines.push("commit;");

writeFileSync("supabase/migrations/0011_seed_bootstrap.sql", lines.join("\n") + "\n");
console.log(`Generated ${events.length} seed events.`);
